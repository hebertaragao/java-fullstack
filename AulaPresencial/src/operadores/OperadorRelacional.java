package operadores;

public class OperadorRelacional {

	public static void main(String[] args) {
		
		int valor1 = 17;
		int valor2 = 18;
		
		boolean resultado = valor1 > valor2;
		System.out.println(resultado);
		
		resultado = valor1 < valor2;
		System.out.println(resultado);
		
		resultado = valor1 >= valor2;
		System.out.println(resultado);

		resultado = valor1 <= valor2;
		System.out.println(resultado);
		
		resultado = valor1 == valor2;
		System.out.println(resultado);
		
		resultado = valor1 != valor2;
		System.out.println(resultado);
		

	}

}
