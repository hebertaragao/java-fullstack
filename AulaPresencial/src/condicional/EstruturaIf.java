package condicional;

public class EstruturaIf {

	public static void main(String[] args) {
		
		int numero = 10;
		if(numero %2 == 0) {
			System.out.println("É par!");
		} else {
			System.out.println("Impar");
		}
		
		int idade = 16;
		if(idade >=18) {
			System.out.println("Émaior de idade: ");
		}else {
			System.out.println("É menor de idade");
		}
		
	}

}
